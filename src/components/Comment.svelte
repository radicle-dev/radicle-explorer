<script lang="ts" strictEvents>
  import Author from "@app/components/Author.svelte";
  import Button from "@app/components/Button.svelte";
  import Icon from "@app/components/Icon.svelte";
  import Markdown from "@app/components/Markdown.svelte";
  import Textarea from "@app/components/Textarea.svelte";
  import { formatTimestamp } from "@app/lib/utils";
  import { createEventDispatcher } from "svelte";

  export let id: string | undefined = undefined;
  export let nodeId: string;
  export let timestamp: number;
  export let body: string;
  export let showReplyIcon: boolean = false;
  export let action: "create" | "view" = "view";
  export let caption = "commented";
  export let rawPath: string;

  const dispatch = createEventDispatcher<{ toggleReply: never }>();
</script>

<style>
  .comment {
    margin-bottom: 1rem;
    display: flex;
  }
  .card {
    flex: 1;
    border: 1px solid var(--color-foreground-4);
    border-radius: var(--border-radius);
  }
  .card-header {
    display: flex;
    align-items: center;
    justify-content: space-between;
    padding: 0.5rem 1rem;
    height: 3rem;
  }
  .card-body {
    font-size: var(--font-size-small);
    padding: 0rem 1rem 1rem 1rem;
  }
  .authorship {
    display: flex;
    align-items: center;
    font-size: var(--font-size-tiny);
    color: var(--color-foreground-6);
  }
  .authorship .caption {
    margin-left: 0.2rem;
  }
  .actions {
    display: flex;
    justify-content: flex-end;
  }
  .action {
    display: flex;
    gap: 0.5rem;
  }
</style>

<div class="comment" {id}>
  <div class="card">
    <div class="card-header">
      <div class="authorship">
        <Author {nodeId} />
        <span class="caption">{caption} {formatTimestamp(timestamp)}</span>
      </div>
      <div class="actions">
        {#if showReplyIcon}
          <Button
            variant="text"
            size="tiny"
            on:click={() => dispatch("toggleReply")}>
            <div class="action">
              <Icon name="chat" />
              <span>reply</span>
            </div>
          </Button>
        {/if}
      </div>
    </div>
    <div class="card-body">
      {#if action === "create"}
        <Textarea
          resizable
          bind:value={body}
          on:submit
          placeholder="Leave a comment" />
      {:else if body.trim() === ""}
        <span class="txt-missing">No description.</span>
      {:else}
        <Markdown {rawPath} content={body} />
      {/if}
    </div>
  </div>
</div>
